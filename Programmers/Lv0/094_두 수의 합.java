public class Solution {

    public static void main(String[] args) throws Exception {
        System.out.println(solution(2, 3)); // 5
        System.out.println(solution(100, 2)); // 102
    }

    public static int solution(int num1, int num2) {
        return num1 + num2;
    }
}
